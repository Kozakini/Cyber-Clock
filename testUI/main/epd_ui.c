// ============================================================
//  Rysowanie jednego glifа — zawsze iteruje po 8 bitach
// ============================================================
static void draw_glyph(int x, int y, uint8_t idx, int scale, uint8_t white) {
    if (FONT[idx].w == 0) return;
    const Glyph *g = &FONT[idx];
    for (int row = 0; row < 8; row++) {
        for (int col = 0; col < 8; col++) {
            if (g->rows[row] & (0x80 >> col)) {
                for (int sy = 0; sy < scale; sy++)
                    for (int sx = 0; sx < scale; sx++)
                        ui_px(x + col*scale + sx, y + row*scale + sy, white);
            }
        }
    }
}

// ============================================================
//  Wypisz tekst — obsługa UTF-8 dla znaku stopnia °
// ============================================================
void ui_text(int x, int y, const char *s, int scale, uint8_t white) {
    while (*s) {
        uint8_t idx = (uint8_t)*s;

        // UTF-8: stopień ° = 0xC2 0xB0
        if (idx == 0xC2 && (uint8_t)s[1] == 0xB0) {
            draw_glyph(x, y, 0xB0, scale, white);
            x += (FONT[0xB0].w + 1) * scale;
            s += 2;
            continue;
        }

        // Pomiń inne bajty UTF-8 > 0x7F
        if (idx > 0x7F) { s++; continue; }

        if (FONT[idx].w > 0) {
            draw_glyph(x, y, idx, scale, white);
            x += (FONT[idx].w + 1) * scale;
        } else {
            x += 5 * scale; // nieznany znak = spacja
        }
        s++;
    }
}

int ui_text_width(const char *s, int scale) {
    int w = 0;
    while (*s) {
        uint8_t idx = (uint8_t)*s;
        if (idx == 0xC2 && (uint8_t)s[1] == 0xB0) {
            w += (FONT[0xB0].w + 1) * scale;
            s += 2; continue;
        }
        if (idx > 0x7F) { s++; continue; }
        w += (FONT[idx].w > 0 ? FONT[idx].w + 1 : 5) * scale;
        s++;
    }
    return w > 0 ? w - scale : 0;
}

// ============================================================
//  Ikony pogody — wektorowe prymitywami
// ============================================================
static void draw_circle_fill(int cx, int cy, int r, uint8_t white) {
    for (int dy = -r; dy <= r; dy++)
        for (int dx = -r; dx <= r; dx++)
            if (dx*dx + dy*dy <= r*r)
                ui_px(cx+dx, cy+dy, white);
}

static void draw_circle_outline(int cx, int cy, int r, uint8_t white) {
    int x = r, y = 0, err = 0;
    while (x >= y) {
        ui_px(cx+x,cy+y,white); ui_px(cx+y,cy+x,white);
        ui_px(cx-y,cy+x,white); ui_px(cx-x,cy+y,white);
        ui_px(cx-x,cy-y,white); ui_px(cx-y,cy-x,white);
        ui_px(cx+y,cy-x,white); ui_px(cx+x,cy-y,white);
        y++;
        err += 1 + 2*y;
        if (2*(err-x)+1 > 0) { x--; err += 1-2*x; }
    }
}

