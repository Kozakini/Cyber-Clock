// ============================================================
//  Rysowanie BMP 1-bit (bit7 = lewy piksel)
// ============================================================
void ui_bmp(int x, int y, int w, int h, const uint8_t *buf) {
    if (!buf) return;
    int row_bytes = (w + 7) / 8;
    for (int row = 0; row < h; row++)
        for (int col = 0; col < w; col++) {
            uint8_t bit = (buf[row * row_bytes + (col >> 3)] >> (7 - (col & 7))) & 1;
            ui_px(x + col, y + row, bit);
        }
}
void ui_text_wrap(int x, int y, const char *s, int scale, int max_width, uint8_t white) {
    char word[32];
    int cx = x;
    int cy = y;
    int line_h = 8 * scale + 3;

    while (*s) {
        // Zbierz jedno słowo
        int wi = 0;
        while (*s && *s != ' ' && wi < 31)
            word[wi++] = *s++;
        word[wi] = '\0';
        if (*s == ' ') s++;

        int ww = ui_text_width(word, scale);

        // Czy słowo się mieści w bieżącej linii?
        if (cx > x && cx + ww > x + max_width) {
            cx = x;
            cy += line_h;
        }

        ui_text(cx, cy, word, scale, white);
        cx += ww + (FONT[' '].w + 1) * scale;
    }
}
