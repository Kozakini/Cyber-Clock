// ============================================================
//  Bufor ekranu
// ============================================================
uint8_t canvas[FULL_SIZE];

// ============================================================
//  Prymitywy rysowania
// ============================================================
void ui_fill(uint8_t white) {
    memset(canvas, white ? 0xFF : 0x00, FULL_SIZE);
}

void ui_px(int x, int y, uint8_t white) {
    if (x < 0 || x >= EPD_W || y < 0 || y >= EPD_H) return;
    int b = y * FULL_W_BYTES + (x >> 3);
    int k = 7 - (x & 7);
    if (white) canvas[b] |=  (1 << k);
    else       canvas[b] &= ~(1 << k);
}

void ui_hline(int x, int y, int len, uint8_t white) {
    for (int i = x; i < x + len; i++) ui_px(i, y, white);
}

void ui_vline(int x, int y, int len, uint8_t white) {
    for (int i = y; i < y + len; i++) ui_px(x, i, white);
}

void ui_rect_fill(int x, int y, int w, int h, uint8_t white) {
    for (int row = y; row < y + h; row++)
        ui_hline(x, row, w, white);
}

void ui_rect_border(int x, int y, int w, int h, uint8_t white) {
    ui_hline(x, y,         w, white);
    ui_hline(x, y + h - 1, w, white);
    ui_vline(x,         y, h, white);
    ui_vline(x + w - 1, y, h, white);
}

