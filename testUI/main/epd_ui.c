        int temp_int  = (int)w->temp_c;
        int temp_frac = (int)(fabsf(w->temp_c - (float)temp_int) * 10.0f);
        snprintf(tstr, sizeof(tstr), "%d.%d\xC2\xB0""C", temp_int, temp_frac);

        int tscale = 6;
        int tx = wx;
        int ty = icon_y + icon_size;
        ui_text(tx, ty, tstr, tscale, 0);

        // Opis
        int desc_y = ty + 7*tscale + 6;
        ui_text_wrap(tx, desc_y, w->description, 2, ZONE_WEATHER_W - MARGIN*2, 0);


        // Miasto
        int city_y = desc_y + 46;
        ui_text(tx, city_y, w->city, 2, 0);

